    int TipoCalculo = 0;
    
    
    void VerificarBotao(){
        String TipoPorcentage;
        if(TipoCalculo>4)TipoCalculo = 0;
        switch(TipoCalculo){
            case 1:
                TipoPorcentage = "-";
                break;
            case 2:
                TipoPorcentage = "x";
                break;
            case 3:
                TipoPorcentage = "รท";
                break;
            case 4:
                TipoPorcentage = "%";
                break;
            default:
                TipoPorcentage = "+";
                break;
        }
        BotaoCalculo.setText(TipoPorcentage);
    }
    
    int ResultadoConta(int valor1, int valor2){
        int valortotal = 0;
        switch(TipoCalculo){
            case 1:
                valortotal = valor1 - valor2;
                break;
            case 2:
                valortotal = valor1 * valor2;
                break;
            case 3:
                valortotal = valor1 / valor2;
                break;
            case 4:
                valortotal = (valor1 * valor2)/100;
                break;
            default:
                valortotal = valor1 + valor2;
                break;
        }
        return valortotal;
    }
